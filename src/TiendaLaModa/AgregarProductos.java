    private void BotonAgregarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarProductosActionPerformed
        String sql = "INSERT INTO Productos VALUES (?,?,?,?,?)";

        try{

            pst = connection.prepareStatement(sql);
            pst.setString(1, CodigoIngresado.getText());//Se obtiene el valor del campo basado en la variable, no la BD
            pst.setString(2, TipoIngresado.getText());
            pst.setString(3, PrecioIngresado.getText());
            pst.setString(4, ColorIngresado.getText());
            pst.setString(5, CantidadIngresada.getText());
            int resultado = pst.executeUpdate();

            if(resultado == 1){
                JOptionPane.showMessageDialog(null, "Producto agregado exitosamente");

                ModuloProductos newframe = new ModuloProductos();
                newframe.setVisible (true);
                this.dispose();
            }
            pst.close();
            connection.close(); //IMPORTANTE:  Aqui se debe cerrar la base de datos
            // para poder usarla en otro modulo
            //si no se cierra en cada uso, nos da error de bloqueo

        }
        catch (Exception e) {
            System.out.print("Error: " + e.getMessage());

        }
    }//GEN-LAST:event_BotonAgregarProductosActionPerformed

